#!/bin/bash

# Скрипт для очистки кэша пакетного менеджера APT

echo "Очистка кэша APT..."

# Обновляем список пакетов (опционально, но рекомендуется)
echo "Обновление списка пакетов..."
sudo apt update

# Удаляем ненужные зависимости и пакеты
echo "Удаление ненужных зависимостей..."
sudo apt autoremove -y

# Очистка кэша скачанных .deb файлов
echo "Очистка кэша скачанных пакетов..."
sudo apt clean

# Альтернатива: apt autoclean — удаляет только устаревшие пакеты
# sudo apt autoclean

echo "Очистка завершена!"

# Показываем текущий размер кэша (для информации)
echo "Текущий размер кэша APT:"
du -sh /var/cache/apt/archives/

exit 0
